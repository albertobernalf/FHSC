SELECT C.CITNUM, C.CITFEC, C1.CITFAC, M.MATOTF
FROM HOSVITAL.CITMED C, HOSVITAL.CITMED1 C1, HOSVITAL.MAEATE M
WHERE C.CITFEC >= '2020-12-01' AND C.CITFEC <= '2020-12-31' AND
      C.CITNUM= C1.CITNUM AND C.CITESTP NOT IN ('N','I') AND C.CITPRO != '890302-P' AND
    C1.CITFAC= M.MPNFAC 

ORDER BY C.CITNUM;

SELECT YEAR(C.CITFEC), MONTH(C.CITFEC),SUM(M.MATOTF), COUNT(*)
FROM HOSVITAL.CITMED C, HOSVITAL.CITMED1 C1, HOSVITAL.MAEATE M
WHERE C.CITFEC >= '2021-07-01' AND C.CITFEC <= '2021-07-31' AND
      C.CITNUM= C1.CITNUM AND C.CITESTP NOT IN ('N','I') AND C.CITPRO != '890302-P' AND   C1.CITFAC= M.MPNFAC
GROUP BY YEAR(C.CITFEC), MONTH(C.CITFEC);



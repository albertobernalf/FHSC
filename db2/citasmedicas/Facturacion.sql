SELECT C.CITNUM, C.CITFEC, C1.CITFAC
FROM HOSVITAL.CITMED C, HOSVITAL.CITMED1 C1
WHERE C.CITFEC >= '2019-05-01' AND C.CITFEC <= '2019-05-31' AND
      C.CITNUM= C1.CITNUM AND C.CITESTP NOT IN ('N','I') AND C.CITPRO != '890302-P'

ORDER BY C.CITNUM;



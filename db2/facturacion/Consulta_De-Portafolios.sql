SELECT HOSVITAL.MAEPRO.PRCODI AS CODIGO, HOSVITAL.PORTAR1.PTCODI, HOSVITAL.PORTAR.PTDESC AS PORTAFOLIO, HOSVITAL.HOMPROC.TRFCOD AS TARIFA, HOSVITAL.HOMPROC.HOMPRODSC AS DESCRIPCION,
 HOSVITAL.HOMPROC.HOMPROVLR  AS VALOR, HOSVITAL.PORTAR1.PTPORC AS PORCENTAGE, ((HOSVITAL.HOMPROC.HOMPROVLR*HOSVITAL.PORTAR1.PTPORC)/100) AS VALOR
FROM HOSVITAL.MAEPRO 
INNER JOIN HOSVITAL.PORTAR1 ON (HOSVITAL.MAEPRO.PRCODI = HOSVITAL.PORTAR1.PRCODI) 
INNER JOIN HOSVITAL.HOMPROC ON (HOSVITAL.PORTAR1.PRCODI = HOSVITAL.HOMPROC.PRCODI) 
INNER JOIN HOSVITAL.PORTAR ON (HOSVITAL.PORTAR1.PTCODI = HOSVITAL.PORTAR.PTCODI) 
WHERE HOSVITAL.HOMPROC.TRFCOD='9' AND HOSVITAL.PORTAR1.PTCODI= '31';

